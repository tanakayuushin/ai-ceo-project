def build_chapter_header(text):
    data = [[Paragraph(text, S["chapter"])]]
    t = Table(data, colWidths=[170 * mm])
    t.setStyle(TableStyle([
        ("BACKGROUND", (0, 0), (-1, -1), C_PRIMARY),
        ("TOPPADDING",    (0, 0), (-1, -1), 8),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 8),
        ("LEFTPADDING",   (0, 0), (-1, -1), 10),
    ]))
    return t

def build_template_card(tpl):
    no_str = f"No. {tpl['no']:03d}"
    rows = [
        [Paragraph(no_str, S["template_no"]), ""],
        [Paragraph(tpl["scene"], S["scene"]), ""],
        [Paragraph(tpl["speech"], S["speech"]), ""],
        [Paragraph(tpl["point"], S["point"]), ""],
    ]
    col_widths = [165 * mm, 5 * mm]
    t = Table(rows, colWidths=col_widths)
    t.setStyle(TableStyle([
        ("BACKGROUND", (0, 0), (0, 0), C_LIGHT),
        ("BACKGROUND", (0, 1), (0, 1), C_LIGHT),
        ("BACKGROUND", (0, 2), (0, 2), colors.white),
        ("BACKGROUND", (0, 3), (0, 3), C_GREEN_BG),
        ("BOX",       (0, 0), (0, -1), 0.5, C_DIVIDER),
        ("LINEBELOW", (0, 1), (0, 2), 0.3, C_DIVIDER),
        ("TOPPADDING",    (0, 0), (0, -1), 5),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 5),
        ("LEFTPADDING",   (0, 0), (0, -1), 8),
        ("RIGHTPADDING",  (0, 0), (0, -1), 8),
        ("SPAN", (0, 0), (1, 0)),
        ("SPAN", (0, 1), (1, 1)),
        ("SPAN", (0, 2), (1, 2)),
        ("SPAN", (0, 3), (1, 3)),
    ]))
    return t

